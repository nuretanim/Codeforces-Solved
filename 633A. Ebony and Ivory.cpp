#include <bits/stdc++.h>

using namespace std;
#define endl '\n'
using ll = long long;

int main() {
  int a,b,c;
  cin >> a >> b >> c;

  for (int i= 0;i<=c;i+=a)
  {
    for(int j = 0;j<=c; j+=b)
    {
      if(i+j == c )
      {
        cout <<"Yes"<< endl;
        return;
      }
    }
  }
  cout << "No" <<endl;
 return 0;
}
